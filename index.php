                <h1>What are Operating Systems?</h1><br />
                <p>An operating system is the most important software on the computer. It deals with the momory and processes of the computer. It also manages the software and hardware. It lets the user communicate with the computer; if a computer does not contain an operating system, it will be completely useless.</p>
                <h2>The job of an Os</h2>
                <p>The OS manages all the software and hardware on the computer. More often then not, there are several computer programs running at once; all needing access your computer's central processing unit(CPU), memeory, and storage. The operating systems makes sure each program gets what is needed.</p>
